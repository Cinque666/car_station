package by.station.controller.util;

public class WashPrices {

    private int washPricesId;
    private String vehicleType;
    private Double cost;

    public int getWashPricesId() {
        return washPricesId;
    }

    public void setWashPricesId(int washPricesId) {
        this.washPricesId = washPricesId;
    }

    public String getVehicleType() {
        return vehicleType;
    }

    public void setVehicleType(String vehicleType) {
        this.vehicleType = vehicleType;
    }

    public Double getCost() {
        return cost;
    }

    public void setCost(Double cost) {
        this.cost = cost;
    }
}
